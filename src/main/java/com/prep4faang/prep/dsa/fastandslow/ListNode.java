package com.prep4faang.prep.dsa.fastandslow;

/**
 * Definition for singly-linked list node.
 */
public class ListNode {
    public int val;          // The value stored in the current node
    public ListNode next;    // Pointer to the next node in the list

    public ListNode(int val) {
        this.val = val;
        this.next = null;  // By default, next is null
    }
}
